package pack1;

public class Student {
	
	int studentId;
	String name, surname;
	int age;
	String department;
	
	
	public Student() {
		
	}
	
	public Student(int studentId, String name, String surname, int age, String department) // Constractor
	{
		this.studentId = studentId;
		this.name = name;
		this.surname = surname;
		this.age = age;
		this.department = department;
		
	}
	
}
